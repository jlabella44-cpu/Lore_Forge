"""Content-addressed image cache — dedup provider API calls across reruns.

The cache wraps `images.generate()`: on a hit we copy bytes from
`{renders_dir}/_cache/images/` into the scene's work_dir instead of
calling the provider. Keyed by sha256(provider || model || aspect ||
prompt) so a provider swap invalidates cleanly.
"""
from __future__ import annotations

from datetime import timedelta
from pathlib import Path
from unittest.mock import patch


def test_compute_key_is_deterministic_and_input_sensitive():
    from app.services.image_cache import compute_key

    k1 = compute_key("wanx", "m", "9:16", "a cat")
    assert k1 == compute_key("wanx", "m", "9:16", "a cat")
    assert len(k1) == 64

    # Any input component changing must flip the hash.
    assert k1 != compute_key("dalle", "m", "9:16", "a cat")
    assert k1 != compute_key("wanx", "m2", "9:16", "a cat")
    assert k1 != compute_key("wanx", "m", "1:1", "a cat")
    assert k1 != compute_key("wanx", "m", "9:16", "a cat ")


def test_generate_caches_identical_prompts(client, tmp_path, monkeypatch):
    """Second call with the same prompt must NOT invoke the provider."""
    from app.config import settings
    from app.services import images

    monkeypatch.setattr(settings, "renders_dir", str(tmp_path / "renders"))
    monkeypatch.setattr(settings, "image_provider", "wanx")

    calls: list[Path] = []

    def fake_wanx(prompt, out_path, aspect):
        calls.append(Path(out_path))
        Path(out_path).write_bytes(b"\x89PNG\r\n\x1a\npixel-bytes")

    out1 = tmp_path / "scene_a.png"
    out2 = tmp_path / "scene_b.png"

    with patch("app.services.images._wanx_generate", side_effect=fake_wanx):
        images.generate("a moonlit circus tent", out1, aspect="9:16")
        images.generate("a moonlit circus tent", out2, aspect="9:16")

    assert len(calls) == 1, "provider should only fire on the first call"
    assert out1.exists() and out2.exists()
    # Hit copied bytes from the cache blob, so contents match.
    assert out1.read_bytes() == out2.read_bytes()


