def test_generate_invalidates_on_provider_swap(client, tmp_path, monkeypatch):
    """Same prompt under a different provider must re-fire — the cache
    key bakes in provider+model, so a config swap shouldn't serve bytes
    generated by the old provider."""
    from app.config import settings
    from app.services import images

    monkeypatch.setattr(settings, "renders_dir", str(tmp_path / "renders"))

    monkeypatch.setattr(settings, "image_provider", "wanx")
    wanx_calls: list[str] = []

    def fake_wanx(prompt, out_path, aspect):
        wanx_calls.append(prompt)
        Path(out_path).write_bytes(b"WANX")

    dalle_calls: list[str] = []

    def fake_dalle(prompt, out_path, aspect):
        dalle_calls.append(prompt)
        Path(out_path).write_bytes(b"DALLE")

    with (
        patch("app.services.images._wanx_generate", side_effect=fake_wanx),
        patch("app.services.images._dalle_generate", side_effect=fake_dalle),
    ):
        images.generate("same prompt", tmp_path / "w.png")
        monkeypatch.setattr(settings, "image_provider", "dalle")
        images.generate("same prompt", tmp_path / "d.png")

    assert len(wanx_calls) == 1
    assert len(dalle_calls) == 1
    assert (tmp_path / "w.png").read_bytes() == b"WANX"
    assert (tmp_path / "d.png").read_bytes() == b"DALLE"


