def test_cache_miss_refills_when_blob_deleted(client, tmp_path, monkeypatch):
    """If the cached blob file is gone (manual rm, _cache/ wiped), the
    next call treats it as a miss and re-fires the provider rather than
    silently serving an empty/missing file."""
    from app.config import settings
    from app.services import image_cache, images

    monkeypatch.setattr(settings, "renders_dir", str(tmp_path / "renders"))
    monkeypatch.setattr(settings, "image_provider", "wanx")

    calls: list[str] = []

    def fake_wanx(prompt, out_path, aspect):
        calls.append(prompt)
        Path(out_path).write_bytes(b"\x89PNG")

    with patch("app.services.images._wanx_generate", side_effect=fake_wanx):
        images.generate("p", tmp_path / "a.png")
        # Nuke the cache blob dir but leave the DB row.
        import shutil
        shutil.rmtree(image_cache.cache_root())
        images.generate("p", tmp_path / "b.png")

    assert len(calls) == 2


