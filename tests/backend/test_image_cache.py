# ---------- Prune LRU-stale cache rows ----------


def test_prune_stale_image_cache_removes_old_rows(client, tmp_path, monkeypatch):
    from app.clock import utc_now
    from app.config import settings
    from app.db import SessionLocal
    from app.models import ImageAssetCache
    from app.services import image_cache, render_retention

    monkeypatch.setattr(settings, "renders_dir", str(tmp_path / "renders"))

    blob_old = image_cache.cache_root() / "aa" / "old.png"
    blob_old.parent.mkdir(parents=True, exist_ok=True)
    blob_old.write_bytes(b"old-bytes")

    blob_new = image_cache.cache_root() / "bb" / "new.png"
    blob_new.parent.mkdir(parents=True, exist_ok=True)
    blob_new.write_bytes(b"new-bytes")

    db = SessionLocal()
    try:
        db.add_all([
            ImageAssetCache(
                prompt_hash="old" + "0" * 61,
                provider="wanx", model="m", aspect="9:16",
                file_path=str(blob_old), bytes=9,
                created_at=utc_now() - timedelta(days=90),
                last_used_at=utc_now() - timedelta(days=60),
                hit_count=0,
            ),
            ImageAssetCache(
                prompt_hash="new" + "0" * 61,
                provider="wanx", model="m", aspect="9:16",
                file_path=str(blob_new), bytes=9,
                created_at=utc_now() - timedelta(days=90),
                last_used_at=utc_now() - timedelta(days=1),
                hit_count=5,
            ),
        ])
        db.commit()
    finally:
        db.close()

    db = SessionLocal()
    try:
        res = render_retention.prune_stale_image_cache(db, max_age_days=30)
    finally:
        db.close()

    assert res["removed_count"] == 1
    assert res["freed_bytes"] == 9
    assert not blob_old.exists()
    assert blob_new.exists()

    db = SessionLocal()
    try:
        assert db.get(ImageAssetCache, "old" + "0" * 61) is None
        assert db.get(ImageAssetCache, "new" + "0" * 61) is not None
    finally:
        db.close()


def test_prune_endpoint_includes_image_cache(client, tmp_path, monkeypatch):
    """POST /packages/prune-renders should sweep the image cache too."""
    from app.clock import utc_now
    from app.config import settings
    from app.db import SessionLocal
    from app.models import ImageAssetCache
    from app.services import image_cache

    monkeypatch.setattr(settings, "renders_dir", str(tmp_path / "renders"))

    blob = image_cache.cache_root() / "cc" / "stale.png"
    blob.parent.mkdir(parents=True, exist_ok=True)
    blob.write_bytes(b"x" * 100)

    db = SessionLocal()
    try:
        db.add(ImageAssetCache(
            prompt_hash="c" * 64,
            provider="wanx", model="m", aspect="9:16",
            file_path=str(blob), bytes=100,
            created_at=utc_now() - timedelta(days=90),
            last_used_at=utc_now() - timedelta(days=60),
            hit_count=0,
        ))
        db.commit()
    finally:
        db.close()

    res = client.post("/packages/prune-renders?max_age_days=30")
    assert res.status_code == 200
    body = res.json()
    assert "image_cache" in body
    assert body["image_cache"]["removed_count"] == 1
    assert not blob.exists()
